public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello Java!");

        int a = 30;
        int b = 20;
        int sum = a * b;

        System.out.println("sum " + sum);
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a / b);
        System.out.println(a * b);
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a == b);

        float c = 3.14f;
        double d = 3.14;
        System.out.println(c + d);
        System.out.println(c - d);
        System.out.println(c / d);
        System.out.println(c * d);
        System.out.println(c / d);
        System.out.println(d > c);

    }
}
